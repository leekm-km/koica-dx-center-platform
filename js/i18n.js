class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('koica-lang') || 'en';
        this.init();
    }

    init() {
        // Set initial language
        document.documentElement.lang = this.currentLang;
        this.updateContent();

        // Add language toggle listeners
        document.addEventListener('DOMContentLoaded', () => {
            const langToggles = document.querySelectorAll('.lang-toggle');
            langToggles.forEach(toggle => {
                toggle.addEventListener('click', (e) => {
                    e.preventDefault();
                    this.toggleLanguage();
                });
            });
        });
    }

    toggleLanguage() {
        this.currentLang = this.currentLang === 'ko' ? 'en' : 'ko';
        localStorage.setItem('koica-lang', this.currentLang);
        document.documentElement.lang = this.currentLang;
        this.updateContent();

        // Update toggle button text
        const langToggles = document.querySelectorAll('.lang-toggle');
        langToggles.forEach(toggle => {
            toggle.textContent = this.currentLang === 'ko' ? 'EN' : '한국어';
        });
    }

    updateContent() {
        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.translate(key);

            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        });

        // Update all elements with data-i18n-html attribute (for HTML content)
        document.querySelectorAll('[data-i18n-html]').forEach(element => {
            const key = element.getAttribute('data-i18n-html');
            element.innerHTML = this.translate(key);
        });
    }

    translate(key) {
        return TRANSLATIONS[this.currentLang][key] || key;
    }

    getCurrentLang() {
        return this.currentLang;
    }
}

// Initialize language manager
const langManager = new LanguageManager();
